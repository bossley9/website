#!/usr/bin/python

#SETUP
import turtle, time
t = turtle.Pen()
turtle.title("The Bat")
turtle.bgcolor('#000000')
t.hideturtle()
t.pencolor('#800000')
turtle.setup(1000, 500)
t.penup()
t.goto(0, -75)
t.pendown()
t.left(90)
#START

#RIGHT
t.right(20)
t.forward(45)
t.right(20)
t.forward(60)
t.right(40)
t.forward(50)
t.right(10)
t.forward(120)
t.left(70)
t.forward(60)
t.right(60)
t.forward(50)
t.left(170)
t.forward(210)
t.left(25)
t.forward(70)
t.right(115)
t.forward(60)
t.left(170)
t.forward(30)
t.right(80)

#MID
t.forward(30)


#LEFT
t.right(80)
t.forward(30)
t.left(170)
t.forward(60)
t.right(115)
t.forward(70)
t.left(25)
t.forward(210)
t.left(170)
t.forward(50)
t.right(60)
t.forward(60)
t.left(70)
t.forward(120)
t.right(10)
t.forward(50)
t.right(40)
t.forward(60)
t.right(20)
t.forward(45)
t.right(20)

#EXTRA
t.right(180)
t.penup()
t.forward(100)
t.pendown()
t.pencolor('#616161')
t.write("I'm Batman.", False, align="center", font=("Arial", 10, "normal"))
time.sleep(1.5)
t.right(180)
t.penup()
t.pencolor('#FF0000')
t.forward(150)
t.pendown()
t.write("Click to exit", False, align="center", font=("Arial", 14, "normal"))

turtle.exitonclick()
